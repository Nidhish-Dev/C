#include<stdio.h>

int main(){
    //integer
    int a =69;

    //float for decimal
    float b = 6.9;

    //character
    //condition : use single quotes, do not use double quotes 
    char c = 'h';

    printf("The value of a is %d\n", a);
    printf("The value of b is %f\n", b);
    printf("The value of c is %c\n", c);
    return 0;

}